#include "libft.h"

int						ft_vhod_elem(char *s, char *d)
{
	char		*tmp;

	tmp = d;
	while (*s != '\0')
	{
		while (*d != '\0')
		{
			if (*s == *d)
				return (1);
			else
				d += 1;
		}
		s += 1;
		d = tmp;
	}
	return (0);
}
