#include "libft.h"

char	*ft_strncat(char *d, const char *s, size_t n)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = ft_strlen(d);
	while ((i < n) && (s[i] != '\0'))
		d[j++] = s[i++];
	d[j] = '\0';
	return (d);
}
